// פונקציה לבדיקת מילים אסורות
export const containsForbiddenWords = (text: string): boolean => {
    if (!text) return false;
    const normalizedText = text.toLowerCase().trim();
    return forbiddenWords.some(word =>
        normalizedText.includes(word.toLowerCase())
    );
};

// פונקציה לבדיקת מילים אסורות באנגלית
export const containsForbiddenEnglishWords = (text: string): boolean => {
    if (!text) return false;
    const normalizedText = text.toLowerCase().trim();
    return forbiddenEnglishWords.some(word =>
        normalizedText.includes(word.toLowerCase())
    );
};

// פונקציה לוולידציית שם מלא בעברית
export const validateHebrewName = (name: string): boolean => {
    if (!name) return false;
    // מותרות אותיות בעברית, מקף, גרש, גרשיים, רווחים, מספרים וסימנים
    const hebrewNameRegex = /^[\u0590-\u05FF\s\-'״0-9&.()]+$/;
    return hebrewNameRegex.test(name.trim());
};

// פונקציה לוולידציית תפקיד בעברית
export const validateHebrewRole = (role: string): boolean => {
    if (!role) return false;
    // מותרות אותיות בעברית ואנגלית, מקף, גרש, גרשיים, רווחים, מספרים וסימנים
    const hebrewRoleRegex = /^[\u0590-\u05FFa-zA-Z\s\-'״0-9&.(),]+$/;
    return hebrewRoleRegex.test(role.trim());
};

// פונקציה לוולידציית אימייל
export const validateEmail = (email: string): boolean => {
    if (!email) return false;
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return emailRegex.test(email);
};

// פונקציה לוולידציית טלפון ישראלי
export const validateIsraeliPhone = (phone: string): boolean => {
    if (!phone) return false;

    // הסרת רווחים, מקפים וסוגריים
    const cleanPhone = phone.replace(/[\s\-\(\)]/g, '');

    // פורמטים נתמכים:
    // 0501234567, 0512345678, 0521234567, 0531234567, 0541234567, 0551234567, 0581234567
    // 021234567, 031234567, 041234567, 071234567, 081234567, 091234567
    // 0761234567, 0771234567, 0781234567, 0791234567
    const mobileRegex = /^(05[0-9]|058)\d{7}$/;
    const landlineRegex = /^(0[2-9]|07[0-9])\d{7}$/;

    return mobileRegex.test(cleanPhone) || landlineRegex.test(cleanPhone);
};

// פונקציה לפורמט טלפון ישראלי
export const formatIsraeliPhone = (phone: string): string => {
    if (!phone) return '';

    // הסרת רווחים, מקפים וסוגריים
    const cleanPhone = phone.replace(/[\s\-\(\)]/g, '');

    // הוספת 0 בהתחלה אם חסר
    const phoneWithZero = cleanPhone.startsWith('0') ? cleanPhone : `0${cleanPhone}`;

    // פורמט לפי סוג הטלפון
    if (/^05[0-9]|058/.test(phoneWithZero)) {
        // טלפון נייד: 050-1234567
        return `${phoneWithZero.slice(0, 3)}-${phoneWithZero.slice(3)}`;
    } else if (/^0[2-9]|07[0-9]/.test(phoneWithZero)) {
        // טלפון נייח: 02-1234567
        return `${phoneWithZero.slice(0, 2)}-${phoneWithZero.slice(2)}`;
    }

    return phoneWithZero;
};
